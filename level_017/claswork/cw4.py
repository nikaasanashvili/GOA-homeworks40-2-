# ეს ელემენტები სიიდან და დააბრუნეთ ძველი სიასადაც აღარ გვექნება ამოშლილი სიტყვები რომელთა სიგრძეც ნაკლები იყო 5 ზე


def less_5(name):
    list_1=[]
    list_2=[]
    for i in name:
        if len(i) > 5:
            list_1.append(i)
        else:
            list_2.append(i)
    return list_1 , list_2
print(less_5(['nika', 'saba', 'nikusha', 'daviti', 'dach']))
