# იყოს 1000 + 900 + 80 (მარტო 1980-ზე არ განიხილოთ ეგ, მაგალითად მოვიყვანე რომ მიხვდეთ ! )

def dashla(nam):
    nam=str(nam)[::-1]
    list=[]
    index=0
    for i in nam:
        if i != '0':
            nam1=i + '0' * index
            list.append(nam1) 
        index += 1
    list.reverse()
    return '+' .join(list)
print(dashla(1980))