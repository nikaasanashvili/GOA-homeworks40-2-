# 1) ჩამოწერეთ and / or - ზე ყველა შესაძლო ვარიანტი და გვერდზე კომენტარის სახით მიუწერეთ რას გამოიტანს შედეგად.

# true and true  გამოიტანს true
# true and false გამოიტანს  false
# false and false გამოიტანს false

# true or true  გამოიტანს  true
# true or false გამოიტანს  true
# false or false გამოიტანს fals